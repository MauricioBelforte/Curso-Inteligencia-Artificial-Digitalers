# Función para sumar dos números

# Definición de una función en Python
# En Python, se define una función utilizando la palabra clave 'def', seguida del nombre de la función, paréntesis y dos puntos.
# Aquí hay un ejemplo de una función que suma dos números:
def suma(a, b):
    """Suma dos números y devuelve el resultado."""
    return a + b        
# Llamada a la función suma
resultado = suma(3, 5)

# Imprimir el resultado de la suma
print("El resultado de la suma es:", resultado)
