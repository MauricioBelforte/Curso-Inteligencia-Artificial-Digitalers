# Operadores aritméticos en Python

# Operadores aritméticos básicos
suma = 5 + 3          # Suma
resta = 10 - 4        # Resta
multiplicacion = 7 * 6  # Multiplicación
division = 20 / 4      # División
modulo = 10 % 3        # Módulo (resto de la división)
potencia = 2 ** 3      # Potencia (2 elevado a 3)
# División entera
division_entera = 15 // 4  # División entera (sin decimales)
# Incremento y decremento
x = 10
x += 5  # Incremento (equivalente a x = x + 5)
x -= 3  # Decremento (equivalente a x = x - 3)


# Imprimir resultados
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Módulo:", modulo)
print("Potencia:", potencia)
print("División entera:", division_entera)  
print("Valor de x después de incremento y decremento:", x)
