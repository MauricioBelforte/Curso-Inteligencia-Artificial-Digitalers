# Condicionales en Python

# if/else/elif son estructuras de control que permiten ejecutar diferentes bloques de código basados en condiciones.
# La palabra reservada elif, que nos permite agregar más de una condición en un mismo condicional:
# Aquí hay un ejemplo de cómo usar estas estructuras:

a = 12

if a > 10:
    print("El valor de a es mayor que 10")
elif a == 10:
    print("El valor de a es igual a 10")
else:
    print("El valor de a es menor que 10")

b = 5
if b > 10:
    print("El valor de b es mayor que 10")
elif b == 10:
    print("El valor de b es igual a 10")
else:
    print("El valor de b es menor que 10")


