# Sets (conjuntos)
# Python incluye un tipo de colección llamado conjunto(set), que es una colección no ordenada y sin elementos repetidos.
# Los usos básicos de los conjuntos incluyen verificación de pertenencia y eliminación de entradas duplicadas. 
# Sin embargo, los conjuntos no son muy empleados en el día a día como las listas, tuplas y diccionarios. 

# Definimos un conjunto con llaves {} o con la función set()
conjunto1 = {1, 2, 3, 4, 5}
conjunto2 = set([1, 2, 3, 4, 5])