""" 
Ejercicio 2
Realizar una función llamada area_rectangulo
que reciba la base y la altura por argumento, y
que devuelva (retorne) el área del rectángulo.
 """

def area_rectangulo(base, altura):
    return base * altura


# Prueba de la función
area = area_rectangulo(5, 10)
print(f"El área del rectángulo es: {area}")
