# Aplicacion de escritorio basica

import tkinter as tk

# Crear la ventana principal
ventana = tk.Tk()
# config permite configurar el tamaño de la ventana en pixeles
ventana.config(width=400, height=300)
# titel se usa para darle un título de la ventana
ventana.title("Pantalla principal")


# Ejecutar la aplicación
ventana.mainloop()